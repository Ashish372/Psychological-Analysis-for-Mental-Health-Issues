function showTextBox(e) {
    console.log('hiii')
    if (e.target.tagName == 'INPUT') {
        const parent = e.target.parentElement.parentElement;
        console.log(parent)
        const radios = parent.getElementsByClassName('form-check-input');
        console.log(radios)
        const textBox = parent.getElementsByClassName('text-box');
        if (radios[0].checked) {
            textBox[0].style.display = 'block';
        } else {
            textBox[0].style.display = 'none';
        }
    }

}